const flightSchema = new mongoose.Schema({
  flightNumber: { type: String, required: [true, "Flight number required"] },
  airline: {
    type: String,
    enum: [
      "Air India",
      "IndiGo",
      "SpiceJet",
      "Vistara",
      "Go First",
      "AirAsia India",
      "Alliance Air",
      "TruJet",
      "Star Air",
      "Air India Express",
      "FlyBig",
      "Akasa Air",
      "Zoom Air",
      "Air Odisha",
      "Air Deccan",
      "Pawan Hans",
    ],
    required: [true, "Airline name required"],
  },
  source: {
    type: String,
    required: [true, "Place of boarding required"],
  },
  destination: {
    type: String,
    required: [true, "Place of deplaning required"],
  },
  departureTime: {
    type: Date,
    required: [true, "Flight departure time required"],
  },
  arrivalTime: {
    type: Date,
    required: [true, "Flight arrival time required"],
  },
  cost: {
    type: Number,
    required: [true, "Cost per person required"],
  },
  currency: {
    type: String,
    enum: currencyType,
    default: "INR",
  },
});

module.exports = mongoose.model("Flight", flightSchema, "flights");
